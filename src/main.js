import { createApp } from 'vue'
import { createPinia } from 'pinia'
import App from './App.vue'
import router from './router'
import './style.css'

// =====================================================
// 1. 创建 Vue 应用实例
// =====================================================
const app = createApp(App)

// =====================================================
// 2. 注册 Pinia 状态管理
// =====================================================
app.use(createPinia())

// =====================================================
// 3. 注册 Vue Router 路由系统
// =====================================================
app.use(router)

// =====================================================
// 4. 挂载应用
// =====================================================
app.mount('#app')
